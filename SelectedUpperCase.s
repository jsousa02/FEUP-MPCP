.text
.global SelectedUpperCase
.type SelectedUpperCase, "function"

SelectedUpperCase:	MOV 	W9, 0 // CONTADOR
OUTER:				LDRB 	W10, [X0] // PRIMEIRA SEQ
					CBZ 	W10, FIM // VERIFICAR SE 1ª SEQ CHEGOU AO FIM
					ADD 	X0, X0, 1 // AVANÇAR PARA O PROXIMO CHAR DA 1ª SEQ
					MOV 	X13, X1 // COPIA DO ENDEREÇO BASE DA 2ª SEQ

INNER:				LDRB 	W12, [X13] // FIX QUANDO ENCONTRA UM WS
					CBZ 	W12, OUTER // VERIFICAR SE 2ª SEQ CHEGOU AO FIMb
					CMP 	W10, W12 // COMPARAR CHAR DA 2ª SEQ COM CHARS DA 1ª
					B.EQ 	TROCAR
					ADD 	X13, X13, 1
					B 		INNER

TROCAR:				SUB 	W12, W12, 32 // CONVERTE PARA LETRA MAIUSCULA
					STRB	W12, [X13] // ALTERA A SEQ
					ADD 	X13, X13, 1 // AVANÇA PARA O PROXIMO CHAR
					ADD 	W9, W9, 1 // INCREMENTAR CONTADOR
					B 		INNER
FIM: 				MOV 	W0, W9
					RET
